console.log('hello, world'); // Console.log() ele exibe mensagens no console do navegador, sendo uma ferramenta para a depuração(debugging) do código.
console.log("hello, world"); // Strings = são textos que ficam entre aspas simples ou duplas.
console.log(`hello, world`); // Template literals são strings que permitem expressões embutidas, usando crases (``) em vez de aspas simples ou duplas.
console.log("'hello, world'"); // As aspas simples dentro da string são exibidas como parte do texto.

console.log(123456); // Números = são valores numéricos, que podem ser inteiros ou decimais.
console.log(12.3456); // Números decimais são representados com um ponto (.) como separador decimal.
console.log(35, 25.85, 'hello, world'); // Múltiplos argumentos podem ser passados para console.log(), separados por vírgulas.
