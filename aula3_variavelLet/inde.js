// Não podemos criar variáveis com palavras reservadas, ex: let console, let if....
// Variáveis precisam ter nomes significativos ex errado: let n = campoBaseDeDado (Você não sabe o que serve esse tipo de variável.)
// Não pode começar o nome de uma variável com um número. ex: let 1nome
// Não podem conter espaços ou traços. ex: let nome-Cliente;
//Utilizamos camelCase. ex: let nomeCompleto;
// Case-sensitive. o modo como escreve pode virar variáveis diferentes. Ex:  'nomeCliente' e 'nomecliente' são duas variaveis diferentes.
// Não podemos redeclarar variáveis com let
// Não utilize var, utitlize let
// O   =    é chamado de de operador de atribuição 

let nome = "João"; // Declara uma variável chamada 'nome' e atribui o valor "João"

console.log(nome); // Imprime o valor da variável 'nome' no console (saída: João)

console.log(nome, 'nasceu em 1984.');
console.log('Em 2000', nome, 'conheceu maria em 2012.');
console.log('Maria teve 1 filho com', nome, 'em 2015.');
console.log('O filho de', nome, 'se chama Eduardo');
